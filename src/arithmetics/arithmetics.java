package arithmetics;

public class arithmetics {

	public static void main(String[] args) {
		int a, b;
		a = 11;
		b = 5;
		int c = a / b;
		int d = a % b;
		System.out.println("Ділення - " +c);
		System.out.println("Залишок від ділення - " +d);
		
		int x;
		x = 60 + 33;
		System.out.println(x);
		
		double y = 2.4;
		System.out.println(Math.round(y));
	}

}
